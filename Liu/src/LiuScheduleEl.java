public class LiuScheduleEl {
    Node node;
    Double time;

    public LiuScheduleEl(Node node, Double time) {
        this.node = node;
        this.time = time;
    }

    public Node getNode() {
        return node;
    }

    public void setNode(Node node) {
        this.node = node;
    }

    public Double getTime() {
        return time;
    }

    public void setTime(Double time) {
        this.time = time;
    }
}
